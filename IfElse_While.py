#using turtle functions for example
import turtle
anushka_turtle=turtle.Turtle()

def square():
  anushka_turtle.forward(100)
  anushka_turtle.right(90)
  anushka_turtle.forward(100)
  anushka_turtle.right(90)
  anushka_turtle.forward(100)
  anushka_turtle.right(90)
  anushka_turtle.forward(100)
  #full square

#IF- ELSE STATEMENTS
elephant_weight= 3000
ant_weight=0.1

if(elephant_weight>ant_weight):
   square()
else:
   anushka_turtle.forward(100)
   
   
